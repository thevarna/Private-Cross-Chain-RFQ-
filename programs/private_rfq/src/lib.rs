//! # Private Cross-Chain RFQ Desk — Program Entrypoint
//!
//! This is the main entrypoint and instruction dispatcher for the `private_rfq`
//! Solana program. Built on **Pinocchio** for compatibility with both the
//! Encrypt (FHE) and Ika (dWallet) pre-alpha SDKs, which cannot share a common
//! Anchor framework version.
//!
//! ## Instruction Dispatch Table
//!
//! | Discriminator | Instruction              | Purpose                                  |
//! |:---:|:---|:---|
//! | `0`           | `initialize_rfq`         | Maker opens an encrypted RFQ             |
//! | `1`           | `submit_bid`             | Taker submits bid + locks USDC escrow    |
//! | `2`           | `request_fhe_match`      | CPI to Encrypt `execute_graph`           |
//! | `3`           | `request_match_decrypt`  | CPI to Encrypt `request_decryption`      |
//! | `4`           | `reveal_match`           | Read decryption result; settle or refund |
//! | `5`           | `execute_settlement`     | CPI to Ika `approve_message`             |
//! | `6`           | `force_cancel_timeout`   | Emergency reset after 200-block stall    |
//!
//! ## Zero-Trust Architecture
//!
//! This program never stores or can decrypt plaintext trade parameters.
//! All sensitive data lives exclusively within Encrypt ciphertext accounts.
//! Cross-chain asset custody is enforced by Ika's 2PC-MPC network — not by
//! any centralized authority or administrator key.

