// ─── Entrypoint ───────────────────────────────────────────────────────────────
entrypoint!(process_instruction);

/// Main instruction dispatcher.
///
/// The first byte of `data` is the instruction discriminator. All remaining
/// bytes are passed to the corresponding handler.
pub fn process_instruction(
    program_id: &Address,
    accounts:   &[AccountView],
    data:       &[u8],
) -> ProgramResult {
    let (discriminator, rest) = data
        .split_first()
        .ok_or(ProgramError::InvalidInstructionData)?;

    match *discriminator {
        // ── 0: Maker opens an encrypted RFQ ───────────────────────────────────
        0 => instructions::initialize_rfq::process(program_id, accounts, rest),

        // ── 1: Taker submits an encrypted bid + USDC escrow ───────────────────
        1 => instructions::submit_bid::process(program_id, accounts, rest),

        // ── 2: Request Encrypt to run the matching computation graph ──────────
        2 => instructions::request_fhe_match::process(program_id, accounts, rest),

        // ── 3: Request Encrypt to decrypt the match result ciphertext ─────────
        3 => instructions::request_match_decrypt::process(program_id, accounts, rest),

        // ── 4: Read the decrypted result; settle (true) or refund (false) ─────
        4 => instructions::reveal_match::process(program_id, accounts, rest),

        // ── 5: Maker calls Ika approve_message to authorize cross-chain TX ────
        5 => instructions::execute_settlement::process(program_id, accounts, rest),

        // ── 6: Emergency timeout after FHE computation stalls > 200 blocks ────
        6 => instructions::force_cancel_timeout::process(program_id, accounts, rest),

        _ => Err(ProgramError::InvalidInstructionData),
    }
}
